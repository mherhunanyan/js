/* Գրել ֆունկցիա, որը որպես արգումենտ ստանում է ամբողջ թվային զանգված, և 
վերադարձնում նույն զանգվածը՝ զույգ տարրերը զանգվածի սկզբից, իսկ կենտերը՝ 
վերջից տեղավորած:*/

function foo(a) {
    let arr = a;
    let j = 0;
    for (let i = 0; i < arr.length; ++i) {
        if (arr[i] % 2 === 0) {
            [arr[j], arr[i]] = [arr[i], arr[j]];
            ++j;
        }
    }
    return arr;
}
console.log(foo([1,2,3,4,5,6]));